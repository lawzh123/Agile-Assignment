    private void jbtnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnRegisterActionPerformed
        // TODO add your handling code here:
        //owner details
        String ownerName = jtfOwnerName.getText();
        String ownerIC = jtfOwnerIC.getText();
        String ownerGender = "";
        String ownerPhone = jtfOwnerPhone.getText();
        //restaurant details
        String resName = jtfResName.getText();
        String resPhone = jtfResPhone.getText();
        String resAddr = jtaResAddress.getText();
        String resType = jcbResType.getSelectedItem().toString();
        if (jrbMale.isSelected()) {
            ownerGender = "Male";
        } else if (jrbFemale.isSelected()) {
            ownerGender = "Female";
        }

        if (ownerName.equals("") || ownerIC.equals("") || ownerPhone.equals("") || resName.equals("") || resPhone.equals("") || resAddr.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill up all the information to proceed.", "Empty field found", JOptionPane.ERROR_MESSAGE);
        } else if (CheckPhone(ownerPhone) && CheckPhone(resPhone)) {
            //JOptionPane.showMessageDialog(null,"Owner name: "+ownerName+"\nOwner Gender: "+ownerGender+"\nOwner IC: "+ownerIC+"\nOwner phone: "+ownerPhone);
            //JOptionPane.showMessageDialog(null,"RegisterRestaurant name: "+resName+"\nRestaurant type: "+resType+"\nRestaurant address: "+resAddr+"\nRestaurant phone: "+resPhone);
            res = new RegisterRestaurant(ownerName, ownerIC, ownerPhone, ownerGender, resName, resType, resAddr, resPhone);
            JOptionPane.showMessageDialog(null, res.toString());
        }
    }//GEN-LAST:event_jbtnRegisterActionPerformed

