    private boolean CheckPhone(String ph) {
        String[] cut;
        try {
            cut = ph.split("[-]");
            Integer.parseInt(cut[0]);
            Integer.parseInt(cut[1]);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Phone number should only contains numbers and a \'-\'."
                    + "\nExample : 016-12345678", "Invalid phone number", JOptionPane.ERROR_MESSAGE);
            return false;
        } catch (ArrayIndexOutOfBoundsException x) {
            JOptionPane.showMessageDialog(null, "Phone number should only contains numbers and a \'-\'."
                    + "\nExample : 016-12345678", "Invalid phone number", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (cut.length != 2 || cut[0].length() != 3 || cut[1].length() < 7 || cut[1].length() > 9) {
            JOptionPane.showMessageDialog(null, "Phone number should be in following format."
                    + "\nExample : 016-12345678", "Invalid phone number format", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }

